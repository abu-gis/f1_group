def build_f1_name_prompt_block() -> str:
    lines = [
        "Use these mandatory Russian name forms consistently:",
    ]

    for english_name, russian_name in F1_NAME_GUIDE:
        lines.append(f"- {english_name} -> {russian_name}")

    lines.append("- Never invent alternative spellings of names.")
    return "\n".join(lines)


def normalize_f1_names(text: str) -> str:
    normalized_text = text

    # Сначала исправляем более длинные формы, чтобы имя+фамилия
    # не ломались частичной заменой одной фамилии.
    for source_text, target_text in sorted(
        F1_NAME_NORMALIZATION.items(),
        key=lambda item: len(item[0]),
        reverse=True,
    ):
        pattern = re.compile(rf"\b{re.escape(source_text)}\b", re.IGNORECASE)
        normalized_text = pattern.sub(target_text, normalized_text)

    return normalized_text